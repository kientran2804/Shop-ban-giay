    //-----------SETFORM KHÁCH HÀNG - NGƯỜI DÙNG-------------
    void setForm(KHACHHANG KH) {
        txtdiachi_KH.setText(KH.getDIACHI());
        txttenkhachhang.setText(KH.getHOTEN());
        txtdiachi_kh.setText(KH.getSDT());
        txtDIACHI_DL.setText(KH.getDIACHI());
        txtSDT_DL.setText(KH.getSDT());

    }

    //---------getform khách hàng - người dùng--------
    KHACHHANG getForm() {
        if (Validation()) {
            KHACHHANG KH = new KHACHHANG();
            KH.setDIACHI(txtdiachi_kh.getText());
            KH.setHOTEN(txttenkhachhang.getText());
            KH.setSDT(txtdiachi_kh.getText());
            NGUOIDUNG ND = new NGUOIDUNG();
            ND.setDIACHI(txtDIACHI_DL.getText());
            ND.setSDT(txtSDT_DL.getText());
        }
        return null;
    }

    //-------------TẠO FILLTABLE------------
    void fillTable() {
        DefaultTableModel model = (DefaultTableModel) tblbanghoadon.getModel();
        model.setRowCount(0);
        try {
            List<SANPHAM> list = Dao.selectAll();
            for (SANPHAM HD : list) {
                Object[] row = {
                    HD.getTENSP(), HD.getSOLUONG(), HD.getGIA(), (HD.getSOLUONG() * HD.getGIA())

                };
                model.addRow(row);
            }
        } catch (Exception e) {
            DialogHelper.alert(this, "Lỗi truy vấn dữ liệu");
        }
    }

    //---------------khách hàng-------------------
    boolean Validation() {
        //kiểm tra địa chỉ khách hàng
        if (txtdiachi_KH.getText().length() == 0) {
            DialogHelper.alert(this, "bạn chưa nhập địa chỉ!");
            txtdiachi_KH.setFocusable(true);
            return false;
        }

        // kiểm tra họ tên 
        if (txttenkhachhang.getText().length() == 0) {
            DialogHelper.alert(this, "bạn chưa nhập họ tên tên người dùng!");
            txttenkhachhang.setFocusable(true);
            return false;
        }

        //kiểm tra số điện thoại
        if (txtdiachi_kh.getText().length() == 0) {
            DialogHelper.alert(this, "bạn chưa nhập Số điện thoại!");
            txtdiachi_kh.setFocusable(true);
            return false;
        }

        //-----------------người dùng---------------
        //kiểm tra địa chỉ người dùng
        if (txtDIACHI_DL.getText().length() == 0) {
            DialogHelper.alert(this, "bạn chưa nhập địa chỉ!");
            txtDIACHI_DL.setFocusable(true);
            return false;
        }
        //kiểm tra số điện thoại
        if (txtSDT_DL.getText().length() == 0) {
            DialogHelper.alert(this, "bạn chưa nhập Số điện thoại!");
            txtSDT_DL.setFocusable(true);
            return false;
        }

        return true;
    }

    void edit() {
        String DC = (String) tblbanghoadon.getValueAt(this.row, 0);
        KHACHHANG KH = Dao.selectAll(DC);
        this.setForm(KH);
        //tabs.setSelectedIndex(0);
        this.updateStatus();

    }

    //update cập nhật 
    void update() {
        KHACHHANG HD = getForm();
        if (HD == null) {
            return;
        } else {
            try {
                Dao.update(HD);
                this.fillTable();
                // this.clearForm();
                DialogHelper.alert(this, "đã cập nhật thông tin người dùng thành công!");
            } catch (Exception e) {
                DialogHelper.alert(this, "Cập nhật người dùng thất bại");
            }
        }
    }

    //delete (buttion xóa)
    void delete() {
        if (DialogHelper.confirm(this, "Bạn thực sự muốn xóa người dùng này?")) {
            String KH = txtdiachi_kh.getText();
            try {
                Dao.delete(KH);
                this.load();
                //this.clearForm();
                DialogHelper.alert(this, "Xóa thành công!");
            } catch (HeadlessException e) {
                DialogHelper.alert(this, "Xóa thất bại!");
            }
        }
    }

    void updateStatus() {
        boolean edit = (this.row >= 0);
        boolean first = (this.row == 0);
        // trạng thái form 
        btnsua.setEnabled(edit);
        btnxoa.setEnabled(edit);

    }

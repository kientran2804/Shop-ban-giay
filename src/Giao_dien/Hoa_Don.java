    void load() {
        DefaultTableModel model = (DefaultTableModel) tblbanghoadon.getModel();
        model.setRowCount(0);
        try {
            List<SANPHAM> list = Dao.selectAll();
            for (SANPHAM HD : list) {
                Object[] row = {
                    HD.getTENSP(),
                    HD.getSOLUONG(),
                    HD.getGIA(),
                    HD.getSOLUONG() * HD.getGIA(),};
                model.addRow(row);
            }
        } catch (Exception e) {
            DialogHelper.alert(this, "Lỗi truy vấn dữ liệu!");
        }
    }



    void clearForm(){
        NGUOIDUNG ND = new NGUOIDUNG();
        this.setForm(ND);
        this.row = -1;
        this.updateStatus();
    }
    void edit(){
        String MAND = (String) tblDS_TAIKHOAN.getValueAt(this.row, 0);
        NGUOIDUNG ND = dao.selectById(MAND);
        this.setForm(ND);
        tabs.setSelectedIndex(0);
        this.updateStatus();
        
    }
    void updateStatus(){
        boolean edit = (this.row >= 0);
        boolean first = (this.row == 0);
        boolean last = (this.row == tblDS_TAIKHOAN.getRowCount() -1);
        // trạng thái form 
        txtMANV.setEditable(!edit);
        btnthem.setEnabled(!edit);
        btnsua.setEnabled(edit);
        btnxoa.setEnabled(edit);
        //trạng thái điều hướng
        btndau.setEnabled(edit && !first);
        btnlui.setEnabled(edit && !first);
        btntoi.setEnabled(edit && !last);
        btncuoi.setEnabled(edit && !last);
    }

    //đầu
    void first(){
        this.row = 0;
        this.edit();
    }
    
    //lùi
    void prev(){
        if(this.row > 0){
            this.row --;
            this.edit();
        }
    }
    
    //tới
    void next(){
        if(this.row < tblDS_TAIKHOAN.getRowCount() - 1){
            this.row ++;
            this.edit();
        }
    }
    
    //cuối
    void last(){
        this.row = tblDS_TAIKHOAN.getRowCount() - 1;
        this.edit();
    }



    //TẠO FILLTABLE
    void fillTable(){
        DefaultTableModel model = (DefaultTableModel) tblDS_TAIKHOAN.getModel();
        model.setRowCount(0);
        try{
            List<NGUOIDUNG> list = dao.selectAll();
            for(NGUOIDUNG ND : list){
                Object[] row = {
                    ND.getMAND(), ND.getHOTEN(),ND.getMATKHAU(),ND.getDIACHI(), ND.getSDT(),ND.getEMAIL(),
                    ND.isGIOITINH() ? "NAM" : "NỮ",ND.isChucVu() ? "QUẢN LÝ" : "NHÂN VIÊN"
                    
                };
                model.addRow(row);
            }
        }catch(Exception e){
            DialogHelper.alert(this, "Lỗi truy vấn dữ liệu");
        }
    }
    boolean Validation(){
        // kiểm tra mã nguoi dùng
        if(txtMANV.getText().length() == 0){
            DialogHelper.alert(this, "bạn chưa nhập mã người dùng!");
            txtMANV.setFocusable(true);
            return false;
        }
        // kiểm tra họ tên 
        if(txtTENNV.getText().length() == 0){
            DialogHelper.alert(this, "bạn chưa nhập họ tên tên người dùng!");
            txtTENNV.setFocusable(true);
            return false;
        }
        //mật khẩu
        if(txtMATKHAU.getText().length() == 0){
            DialogHelper.alert(this, "bạn chưa nhập mật khẩu người dùng!");
            txtMATKHAU.setFocusable(true);
            return false;
        }
               
         //kiểm tra địa chỉ
        if(txtDIACHI.getText().length() == 0){
            DialogHelper.alert(this, "bạn chưa nhập địa chỉ!");
            txtDIACHI.setFocusable(true);
            return false;
        }
        
        //kiểm tra số điện thoại
        if(txtSDT.getText().length() == 0){
            DialogHelper.alert(this, "bạn chưa nhập Số điện thoại!");
            txtSDT.setFocusable(true);
            return false;
        }
       //kiểm tra email
       if(txtEMAIL.getText().length() == 0){
            DialogHelper.alert(this, "bạn chưa nhập EMAIL người dùng!");
            txtEMAIL.setFocusable(true);
            return false;
       }
        return true;
    }

    //INSERT (buttion thêm)
    void insert(){
        NGUOIDUNG ND = getForm();
        if(ND == null){
            return;
        }else if(dao.selectById(txtMANV.getText()) == null){
            try{
                dao.insert(ND);
                this.fillTable();
                this.clearForm();
                DialogHelper.alert(this, "đã thêm người dùng thành công!");
            }catch(Exception e){
                DialogHelper.alert(this, "thêm người dùng thất bại");
            }
        }else{
            DialogHelper.alert(this, "mã người dùng đã tồn tại");
        }
    }
    
